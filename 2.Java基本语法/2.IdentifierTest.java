//类名: 采用大驼峰，XxYyZz
class IdentifierTest {
    public static void main(String[] args) {
        //变量名: 采用小驼峰，xxYyZz
        int myNumber = 100;
        System.out.println(myNumber);

        //常量名: 下划线+大写 XX_YY_ZZ
        int MIN_POSITIVE_INT = 0;
        System.out.println(MIN_POSITIVE_INT);
    }
}