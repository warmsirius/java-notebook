class VariableBoolean{
    public static void main(String[] args) {
        boolean b1 = true;
        System.out.println(b1);

        //在条件判断、循环结构中使用
        boolean isMarried = false;
        if (isMarried){
            System.out.println("很遗憾，不能参加单身派对了！");
        }else{
            System.out.println("欢迎来参加单身派对!");
        }
    }
}